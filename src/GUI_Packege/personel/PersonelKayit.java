 void kaydet()
{
int id=SecilenGOREV();
String addRow = "INSERT INTO personel (GOREV_ID,ADI,SOYADI,TELEFON,ADRES,IPTAL"
        + ")"
        + "VALUES('"+id+"','"+ADI.getText()+"','"+SOYADI.getText()+"','"+TLF.getText()+"','"+ADRES.getText()+"',0"
        + ")";  
    try {
            databs.openDatabase();
            databs.sttmnt.execute(addRow);
            JOptionPane.showMessageDialog(null, "Personel Kaydı Yapıldı.");
            Temizle();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog( null,ex.getMessage(), "Database error", JOptionPane.ERROR_MESSAGE );
            Logger.getLogger(DbConnection.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            databs.closeDatabase();
            }
}
