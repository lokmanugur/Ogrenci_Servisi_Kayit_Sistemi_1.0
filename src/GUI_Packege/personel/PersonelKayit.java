public Integer SecilenGOREV()
{
    int id = 0;
        String firTable = "SELECT ID FROM gorev WHERE GOREVI='"+jComboGorev.getSelectedItem()+"'";
    try {
        databs.openDatabase();
        databs.sttmnt.executeQuery(firTable);
         ResultSet rs = databs.sttmnt.getResultSet();
            while((rs!=null) && (rs.next()))
            {
                id = Integer.valueOf(rs.getString("ID"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog( null,ex.getMessage(), "Database error", JOptionPane.ERROR_MESSAGE );
        Logger.getLogger(OgrenciKayit.class.getName()).log(Level.SEVERE, null, ex);
    }finally{
    databs.closeDatabase();}
   return id;
}
 public void gorev_Cagir(){
        LinkedHashSet<String> arraylist=new LinkedHashSet<>(); 
        String firTable = "SELECT GOREVI FROM gorev";
    try {
        databs.openDatabase();
        databs.sttmnt.execute(firTable);
         ResultSet rs = databs.sttmnt.getResultSet();
            while((rs!=null) && (rs.next()))
            {
             String ad= rs.getString("GOREVI");
             arraylist.add(ad);            
            }
            Iterator i=arraylist.iterator();
          jComboGorev.removeAllItems();
            while(i.hasNext()){
           jComboGorev.addItem((Object)i.next());
             } 
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog( null,ex.getMessage(), "Database error", JOptionPane.ERROR_MESSAGE );
        Logger.getLogger(OgrenciKayit.class.getName()).log(Level.SEVERE, null, ex);
    }finally{
    databs.closeDatabase();
    }
} 
